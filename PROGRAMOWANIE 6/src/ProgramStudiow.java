class ProgramStudiow {
    private String nazwaProgramu;
    private String opisProgramu;
    public ProgramStudiow(String nazwaProgramu, String opisProgramu) {
        this.nazwaProgramu = nazwaProgramu;
        this.opisProgramu = opisProgramu;
    }
    // Gettery i settery
    public String getNazwaProgramu() {
        return nazwaProgramu;
    }
    public void setNazwaProgramu(String nazwaProgramu) {
        this.nazwaProgramu = nazwaProgramu;
    }
    public String getOpisProgramu() {
        return opisProgramu;
    }
    public void setOpisProgramu(String opisProgramu) {
        this.opisProgramu = opisProgramu;
    }
}
